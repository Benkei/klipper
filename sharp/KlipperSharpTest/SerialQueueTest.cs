			MemoryStream msg = new MemoryStream(16);

			while (true)
			{
				queue_message qm;
				ser.pull(out qm);

				Console.WriteLine($"MSG {qm.sent_time}/{qm.receive_time} {(int)((qm.receive_time - qm.sent_time) * 1000)}");

